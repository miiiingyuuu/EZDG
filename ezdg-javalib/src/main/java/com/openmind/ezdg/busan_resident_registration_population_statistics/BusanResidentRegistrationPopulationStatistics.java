package com.openmind.ezdg.busan_resident_registration_population_statistics;


public class BusanResidentRegistrationPopulationStatistics {

    private String gugun;
    private Long dongsu;
    private Long generation;
    private Long population;
    private Long malePopulation;
    private Long femalePopulation;
    private Double compositionRatio;
    private Double area;
    private Long populationDensity;

    // Getters and Setters
    public String getGugun() {
        return gugun;
    }

    public void setGugun(String gugun) {
        this.gugun = gugun;
    }

    public Long getDongsu() {
        return dongsu;
    }

    public void setDongsu(Long dongsu) {
        this.dongsu = dongsu;
    }

    public Long getGeneration() {
        return generation;
    }

    public void setGeneration(Long generation) {
        this.generation = generation;
    }

    public Long getPopulation() {
        return population;
    }

    public void setPopulation(Long population) {
        this.population = population;
    }

    public Long getMalePopulation() {
        return malePopulation;
    }

    public void setMalePopulation(Long malePopulation) {
        this.malePopulation = malePopulation;
    }

    public Long getFemalePopulation() {
        return femalePopulation;
    }

    public void setFemalePopulation(Long femalePopulation) {
        this.femalePopulation = femalePopulation;
    }

    public Double getCompositionRatio() {
        return compositionRatio;
    }

    public void setCompositionRatio(Double compositionRatio) {
        this.compositionRatio = compositionRatio;
    }

    public Double getArea() {
        return area;
    }

    public void setArea(Double area) {
        this.area = area;
    }

    public Long getPopulationDensity() {
        return populationDensity;
    }

    public void setPopulationDensity(Long populationDensity) {
        this.populationDensity = populationDensity;
    }

}
