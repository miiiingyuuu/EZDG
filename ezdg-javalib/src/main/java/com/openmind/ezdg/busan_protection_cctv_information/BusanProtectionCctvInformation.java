package com.openmind.ezdg.busan_protection_cctv_information;


public class BusanProtectionCctvInformation {

    private String cctvName;
    private String cctvCoordinates;
    private String cctvGroup;

    // Getters and Setters
    public String getCctvName() {
        return cctvName;
    }

    public void setCctvName(String cctvName) {
        this.cctvName = cctvName;
    }

    public String getCctvCoordinates() {
        return cctvCoordinates;
    }

    public void setCctvCoordinates(String cctvCoordinates) {
        this.cctvCoordinates = cctvCoordinates;
    }

    public String getCctvGroup() {
        return cctvGroup;
    }

    public void setCctvGroup(String cctvGroup) {
        this.cctvGroup = cctvGroup;
    }

}
