package com.openmind.ezdg.BeachInfoservice;

import lombok.Getter;
import lombok.Setter;
import java.util.List;
import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;

@Getter
@Setter
public class SunInfoBeachResponse {

    private Response response;

    @Getter
    @Setter
    public static class Response {
        private Header header;
        private Body body;
    }

    @Getter
    @Setter
    public static class Header {
        private Integer resultCode;
        private String resultMsg;
    }

    @Getter
    @Setter
    public static class Body {
        private String dataType;
        private Items items;
        private String pageNo;
        private String numOfRows;
        private String totalCount;
    }

    @Getter
    @Setter
    public static class Items {
        private List<Item> item;
    }

    @Getter
    @Setter
    public static class Item {
        private String beachNum;
        private String baseDate;
        private String sunrise;
        private String sunset;

        @Override
        public String toString() {
        StringBuilder sb = new StringBuilder();
            sb.append("BeachNum: ").append(this.getBeachNum()).append("\n");
            sb.append("BaseDate: ").append(this.getBaseDate()).append("\n");
            sb.append("Sunrise: ").append(this.getSunrise()).append("\n");
            sb.append("Sunset: ").append(this.getSunset()).append("\n");
            sb.append("-----------------------\n");
            return sb.toString();
        }
    }

    @Override
    public String toString() {
        ObjectMapper mapper = new ObjectMapper();
        try {
             return mapper.writerWithDefaultPrettyPrinter().writeValueAsString(this);
        } catch (JsonProcessingException e) {
            e.printStackTrace();
            return super.toString();
        }
    }
}