package com.openmind.ezdg.gumi_model_restaurant_status;


public class GumiModelRestaurantStatus {

    private Long number;
    private String category;
    private String businessName;
    private String locationRoadNameAddress;
    private String phoneNumber;
    private String representativeMenu;

    // Getters and Setters
    public Long getNumber() {
        return number;
    }

    public void setNumber(Long number) {
        this.number = number;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getBusinessName() {
        return businessName;
    }

    public void setBusinessName(String businessName) {
        this.businessName = businessName;
    }

    public String getLocationRoadNameAddress() {
        return locationRoadNameAddress;
    }

    public void setLocationRoadNameAddress(String locationRoadNameAddress) {
        this.locationRoadNameAddress = locationRoadNameAddress;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getRepresentativeMenu() {
        return representativeMenu;
    }

    public void setRepresentativeMenu(String representativeMenu) {
        this.representativeMenu = representativeMenu;
    }

}
