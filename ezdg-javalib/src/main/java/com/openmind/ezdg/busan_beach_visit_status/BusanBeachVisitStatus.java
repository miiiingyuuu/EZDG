package com.openmind.ezdg.busan_beach_visit_status;


public class BusanBeachVisitStatus {

    private Long byYear;
    private String sido;
    private String gugun;
    private String beachName;
    private Long scale;
    private String numberOfVisitors;
    private String openingDate;
    private String closingDate;
    private String notice;

    // Getters and Setters
    public Long getByYear() {
        return byYear;
    }

    public void setByYear(Long byYear) {
        this.byYear = byYear;
    }

    public String getSido() {
        return sido;
    }

    public void setSido(String sido) {
        this.sido = sido;
    }

    public String getGugun() {
        return gugun;
    }

    public void setGugun(String gugun) {
        this.gugun = gugun;
    }

    public String getBeachName() {
        return beachName;
    }

    public void setBeachName(String beachName) {
        this.beachName = beachName;
    }

    public Long getScale() {
        return scale;
    }

    public void setScale(Long scale) {
        this.scale = scale;
    }

    public String getNumberOfVisitors() {
        return numberOfVisitors;
    }

    public void setNumberOfVisitors(String numberOfVisitors) {
        this.numberOfVisitors = numberOfVisitors;
    }

    public String getOpeningDate() {
        return openingDate;
    }

    public void setOpeningDate(String openingDate) {
        this.openingDate = openingDate;
    }

    public String getClosingDate() {
        return closingDate;
    }

    public void setClosingDate(String closingDate) {
        this.closingDate = closingDate;
    }

    public String getNotice() {
        return notice;
    }

    public void setNotice(String notice) {
        this.notice = notice;
    }

}
